package org.adamsko.cubicforest.world.mapsLoader.tiled;

public class TiledObject {

	private int gid;
	private String name;
	private String type;
	private int x;
	private int y;

	public int getGid() {
		return gid;
	}

	public void setGid(final int gid) {
		this.gid = gid;
	}

	public String getName() {
		return name;
	}

	public void setName(final String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(final String type) {
		this.type = type;
	}

	public int getX() {
		return x;
	}

	public void setX(final int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(final int y) {
		this.y = y;
	}

}
