package org.adamsko.cubicforest.render.cubicModel.jsonLoader.cube;

public class CubeColor {
	private float r;
	private float g;
	private float b;

	public void setR(final float r) {
		this.r = r;
	}

	public float getR() {
		return r;
	}

	public void setG(final float g) {
		this.g = g;
	}

	public float getG() {
		return g;
	}

	public void setB(final float b) {
		this.b = b;
	}

	public float getB() {
		return b;
	}

}
