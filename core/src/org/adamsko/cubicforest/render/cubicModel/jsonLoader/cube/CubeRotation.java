package org.adamsko.cubicforest.render.cubicModel.jsonLoader.cube;

public class CubeRotation {
	private float x;
	private float y;
	private float z;

	public void setX(final float x) {
		this.x = x;
	}

	public float getX() {
		return x;
	}

	public void setY(final float y) {
		this.y = y;
	}

	public float getY() {
		return y;
	}

	public void setZ(final float z) {
		this.z = z;
	}

	public float getZ() {
		return z;
	}
}
