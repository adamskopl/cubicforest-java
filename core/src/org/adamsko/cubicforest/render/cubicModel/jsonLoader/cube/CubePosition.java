package org.adamsko.cubicforest.render.cubicModel.jsonLoader.cube;

public class CubePosition {
	private int x;
	private int y;
	private int z;

	public void setX(final int x) {
		this.x = x;
	}

	public int getX() {
		return x;
	}

	public void setY(final int y) {
		this.y = y;
	}

	public int getY() {
		return y;
	}

	public void setZ(final int z) {
		this.z = z;
	}

	public int getZ() {
		return z;
	}
}
